    private void initTimeFields() {
        try {
            long stime = 0; 
            long etime = 0;
            startTime = Calendar.getInstance();
            endTime = Calendar.getInstance();
            
            stime = myPHeader.StartTime;
            startTime.setTimeInMillis( stime );
            jLabelStart.setText( GUIConst.DEFAULT_DATE_AND_TIME_FORMAT( new Timestamp( stime )));
            jXDatePickerStart.setDate( startTime.getTime() );
            numModelStartHour.setValue( startTime.get( Calendar.HOUR_OF_DAY ) );
            
            long timeMilli = stime + myPHeader.SampleRate * myPHeader.SampleRateFactor * myDB.MAX_EXCEL_LINES_ALLOWED;
            if ( myPHeader.StopTime != NProtocolHeader.DEFAULT_START_AND_END_TIME ) { 
                etime = myPHeader.StopTime;
//                eDate = new Date( etime.getTime() );
                jLabelEnd.setText( GUIConst.DEFAULT_DATE_AND_TIME_FORMAT( new Timestamp( etime )));
//                jXDatePickerEnd.setDate( eDate );
//                if ( etime < timeMilli ) {
                    // the actual endtime is ealier
                    timeMilli = etime;
//                }
            } 
            
            endTime.setTimeInMillis( timeMilli );
            jXDatePickerEnd.setDate( endTime.getTime() );            
            numModelEndHour.setValue( endTime.get( Calendar.HOUR_OF_DAY ) + 1);
           
            try{
                
                if(etime > 0){
                    if((etime/1000) % 3600 == 0){
                       numModelEndHour.setValue(endTime.get( Calendar.HOUR_OF_DAY ));
                    }
                }
                pheaderStartTime = GUIConst.DEFAULT_DATE_FORMAT(new Timestamp( stime )) + " " + numModelStartHour.getNumber().intValue();

                pheaderEndTime = GUIConst.DEFAULT_DATE_FORMAT(new Timestamp( timeMilli )) + " " + numModelEndHour.getNumber().intValue();
            }catch(Exception e){
                pheaderStartTime = null;
                pheaderEndTime = null;
            }
        } catch ( Exception e ) {
            System.out.println( "initTimeFields" + e.getMessage() );
        }
        
    }
    
