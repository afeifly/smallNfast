    private boolean verifySelectedStartTime() {
        // start time
        Calendar c = Calendar.getInstance();
        c.setTime( jXDatePickerStart.getDate() ); 
//System.out.println( "selected date: "  + c.toString() );
        c.set( Calendar.HOUR_OF_DAY, numModelStartHour.getNumber().intValue() );
        c.set( Calendar.MINUTE, 0 );
        c.set( Calendar.SECOND, 0 );
        c.set( Calendar.MILLISECOND, 0 ); 
        
        if(c.getTimeInMillis() >= endTime.getTimeInMillis()){
            JOptionPane.showMessageDialog(this,java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("Start_time_must_be_before_end_time"));
            return false;
        }
        
        if ( myPHeader.StartTime - c.getTimeInMillis() >= myDB.ONE_HOUR_MILLS ||
                c.getTimeInMillis() > myPHeader.StopTime )  {
            
            if(pheaderStartTime != null && pheaderEndTime != null){
                JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("The_valid_record_time_is_from")
                       + " '" + pheaderStartTime + "' " + java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("starttime_to_endtime")+" '" + pheaderEndTime + "', " + java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("startime_setting_overrange"));
            }
            return false;
        }else if( (myPHeader.StartTime - c.getTimeInMillis()) > 0 
                && (myPHeader.StartTime - c.getTimeInMillis()) < myDB.ONE_HOUR_MILLS){
            c.setTimeInMillis( myPHeader.StartTime );
            jXDatePickerStart.setDate( c.getTime() );
            numModelStartHour.setValue( c.get( Calendar.HOUR_OF_DAY ) );
//            return false;
        }
        
        startTime.setTimeInMillis( c.getTimeInMillis() );
        
        return true;
    }
    
