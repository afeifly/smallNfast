    public int getStartID() {
        int id;
        long timeMilli = startTime.getTimeInMillis();
        long stimeMilli = myPHeader.StartTime;
        if ( timeMilli <= stimeMilli ) {
            id = 0;
        } else {
            id = (int) (( timeMilli - stimeMilli ) / ( myPHeader.SampleRate * myPHeader.SampleRateFactor ) + 1 );
        }
        System.out.println("ExportDataOptionDialog/getStartID="+id);
        return id;
    }
    
    public int getEndID() {
        int id;
        long timeMilli = endTime.getTimeInMillis();
        long stimeMilli = myPHeader.StartTime;

        id = (int) (( timeMilli - stimeMilli ) / ( myPHeader.SampleRate * myPHeader.SampleRateFactor ));
        
        if ( id < 0 )
            id = 0;

        System.out.println("ExportDataOptionDialog/getEndID="+id);
        return id;
    }
    
