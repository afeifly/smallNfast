    public int exportPHeaderToCSV(long pref, String excelFileName, int startID, int endID, boolean shrinkData, boolean replaceExistingFile) {
       
        try {
            // ################ check if record file exists, target file exist #######################
            File file = CSMDF.findRecordFile( pref );
            if ( file == null ) return DBMessage.CSDB_FILE_DOES_NOT_EXIST;

            if ( !excelFileName.endsWith( ".csv") && !excelFileName.endsWith( ".CSV") )
                excelFileName += ".csv";

            File targetFile = new File( excelFileName );
            if ( targetFile.exists() ) {
                if ( !replaceExistingFile )
                    return DBMessage.CSDB_FILE_ALREADY_EXIST;
                else {
                    if ( !targetFile.delete() )
                        return DBMessage.CSDB_FILE_CURRENTLY_OPENED;
                }
            }

            NProtocolHeader pheader = new NProtocolHeader();
            int row = 0;
            String name;       
            long stime;
            ArrayList<NChannelHeader> chheaders = new ArrayList<NChannelHeader>();
            int chSize;
            NChannelHeader chheader;
            jxl.write.Number number;
            long timeDiffPerID;
            long recordStartPosition;
            int recordID;
            int startRecordNo, endRecordNo;
            int lengthPerLine;


            // ######################  read record file information ###############################

            RandomAccessFile ranFileRead = new RandomAccessFile ( file, CSMDF.RANDOM_READ_FILE_ACCESS_MODE );            
            pheader.readFromFile( ranFileRead,  CSMDF.POSITION_PROTOCOL_HEADER );
            stime = pheader.StartTime;
            pheader.StopTime = CSMDF.calculateEndTime(pheader.Pref);
            timeDiffPerID = pheader.SampleRate * pheader.SampleRateFactor;
            lengthPerLine = NMeasurementRecord.ID_LENGTH + pheader.NumOfChannels * NMeasurementRecord.VALUE_LENGTH;

//            for ( int i = 0; i < pheader.NumOfChannels; i++ ) {
//                NChannelHeader ch = new NChannelHeader();
//                ch.readFromFile( ranFileRead, ranFileRead.getFilePointer() );
//                chheaders.add( ch );
//            }
//            chSize = chheaders.size();
//            if ( chSize == 0) {
//                // nothing found
//                ranFileRead.close();
//                return DBMessage.CSDB_NO_DATA_AVAILABLE;
//            }

            ranFileRead.seek( CSMDF.POSITION_RECORD_POSITION_POINTER );
            recordStartPosition= ranFileRead.readLong();

            for ( long position = CSMDF.POSITION_1ST_CHANNEL_HEADER; position < recordStartPosition; 
                                        position += NChannelHeader.LENGTH ) {
                NChannelHeader ch = new NChannelHeader();
                ch.readFromFile( ranFileRead, position );
                chheaders.add( ch );
            }
            chSize = chheaders.size();
            if ( chSize == 0) {
                // nothing found
                ranFileRead.close();
                return DBMessage.CSDB_NO_DATA_AVAILABLE;
            }
            
            // find out start and end record number
            if (startID >= 0 && endID >= 0) {
                //mrecords = queryMeasurementRecordWithinCertainPeriod( chheaders.get(i).cref, startID, endID );
                startRecordNo = startID; endRecordNo = endID;
//                ranFileRead.seek(recordStartPosition + startID * lengthPerLine);
//                recordID = ranFileRead.readInt();
//                while( recordID < startID ) {
//                    ranFileRead.seek(ranFileRead.getFilePointer() - NMeasurementRecord.ID_LENGTH - lengthPerLine);
//                    recordID = ranFileRead.readInt();
//                    startRecordNo -= 1;
//                }
//                ranFileRead.seek(recordStartPosition + endID * lengthPerLine);
//                recordID = ranFileRead.readInt();
//                while( recordID < endID ) {
//                    ranFileRead.seek(ranFileRead.getFilePointer() - NMeasurementRecord.ID_LENGTH - lengthPerLine);
//                    recordID = ranFileRead.readInt();
//                    endRecordNo -= 1;
//                }
            } else {
                //mrecords = queryMeasurementRecord(chheaders.get(i).cref);
                startRecordNo = 0;
                endRecordNo = pheader.NumOfSamples - 1;
            }

            ranFileRead.close();

            // ############ start to create excel file ################################

            FileOutputStream csvFileOut = new FileOutputStream( targetFile );            
            OutputStreamWriter csvFileOutWriter = new OutputStreamWriter(csvFileOut,"UTF-8");            
            BufferedWriter csvFileBufWriter = new BufferedWriter(csvFileOutWriter);            
            String changedOneLine = "\r\n";

            String softwareName = "";
//            if(LicenseConst.EXPORTEXCEL_SOFTWARENAME == null || "".equals(LicenseConst.EXPORTEXCEL_SOFTWARENAME)){
                softwareName = "CAA";
//            }else{
//                softwareName = LicenseConst.EXPORTEXCEL_SOFTWARENAME ;
//            }

//            DateFormat customDateFormat = new DateFormat("MM/dd/yyyy hh:mm:ss");
            SimpleDateFormat sdf= new SimpleDateFormat(shortDateFormatStyle+" HH:mm:ss");
            
            //soon
            csvFileBufWriter.write(softwareName+ "," + EXPORT_FILE_VERSION +changedOneLine);
            csvFileBufWriter.write(changedOneLine);
            csvFileBufWriter.write( "Description"+","+ pheader.getDescription()+","+ changedOneLine );
            
            csvFileBufWriter.write("Start Time" + "," +  sdf.format(pheader.StartTime) +","+ changedOneLine  );

            if ( pheader.StopTime != NProtocolHeader.DEFAULT_START_AND_END_TIME ) {
               csvFileBufWriter.write("End Time" + "," +  sdf.format( pheader.StopTime )+","+ changedOneLine  );
            }

            csvFileBufWriter.write("Sample Rate (sec)" + "," + pheader.SampleRate * pheader.SampleRateFactor / 1000+","+ changedOneLine +changedOneLine+changedOneLine );

            String tilie = "";
            tilie = "TIME";
            String tempUnit = "";
            char cubeSymbol = 0xB3;
            
            for ( int i = 0; i < chSize; i++ ) {
                chheader = chheaders.get( i );
                name = CommonValue.getViewChannelFullName(pheader, chheader);
//                name = chheader.getDescription();
//                tempUnit = chheader.getUnitText(); 
////                tempUnit = new String(tempUnit.getBytes(), "utf-8"); 
//                if(tempUnit != null){
//            
//                    tempUnit = tempUnit.replace(cubeSymbol, '3');
//                }
//                name += "(" + tempUnit + ")";
                tilie +=","+name;
            }
            csvFileBufWriter.write(tilie+changedOneLine);

            // ###########################  now data ################################
            String oneLineData = "";
            int resolution;
            int[] floatFormats = new int[chSize];
            String formatString;
            FileInputStream fileIn = new FileInputStream(file);
            FileChannel fileChannel = fileIn.getChannel();
            byte[] byteDouble = new byte[8];
            double value;
            byte[] byteInt = new byte[4];
            int id;
            int recordRow = row;

            // get formats
            for ( int i = 0; i < chSize; i++ ) {
                chheader = chheaders.get( i );
                // deal with format
                resolution  = chheader.Resolution;
                if(resolution < 0) resolution = 0;
                floatFormats[i] = resolution;
            }

            if (( startID >=0 && endID >= 0 ) || (( startID <= 0 || endID <= 0 ) && !shrinkData )) {
                oneLineData = "";
                fileChannel.position( recordStartPosition + startRecordNo * lengthPerLine );
                for ( int i = startRecordNo; i <= endRecordNo; i++ ) {
                    fileIn.read( byteInt, 0, 4 );
                    id = CSMDF.byteArrayToInt( byteInt );
                    oneLineData = sdf.format( stime + timeDiffPerID * id );
                    for ( int j = 0; j < chSize; j++ ) {
                        fileIn.read( byteDouble, 0, 8 );
                        value = CSMDF.byteArrayToDouble( byteDouble );
                        oneLineData += ","+ String.format("%." + floatFormats[j] + "f", value) ;
                    }
                    oneLineData += changedOneLine;
                    csvFileBufWriter.write(oneLineData);
                } // cycle of record lines

            } else {
            }

            fileIn.close();
            csvFileBufWriter.close();
            csvFileOutWriter.close();
            csvFileOut.close();

        } catch ( Exception e ) {          
            e.printStackTrace();
            return DBMessage.CSDB_EXPORT_FAIL;
        }

        return DBMessage.CSDB_OK;
    }
    
