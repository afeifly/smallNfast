    private void jButtonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOKActionPerformed

        //add by be, on 20090305.
        if(jTextTitle.getText().length() > 51){
           JOptionPane.showMessageDialog(null, java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("The_title's_length_must_be_less_than_50."));
           return ;          
        } 
   
        try{    
            String str = jTextAreaContent.getText().trim();
            Rectangle rec = jTextAreaContent.modelToView(str.length());
           // System.out.println("========================"+rec.y / rec.height);
            if((rec.y / rec.height +1) > 11){
                 JOptionPane.showMessageDialog(null,java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("The_content's_max_rows_is_11."));
                 return ; 
            }
        }catch(BadLocationException  e){
            e.printStackTrace();
        }              
        
        myTexts.CommendPageTitle = jTextTitle.getText();
        myTexts.CommendPageBodyText = jTextAreaContent.getText();
        dispose();
    }//GEN-LAST:event_jButtonOKActionPerformed
    
