    private void jButtonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOKActionPerformed
        FileNameExtensionFilter filter = new FileNameExtensionFilter( ReportFile.REPORT_FILE_SUFFIX, 
                    ReportFile.REPORT_FILE_SUFFIX_FILTER, ReportFile.REPORT_FILE_SUFFIX_CAPTICAL_FILTER );
        GUIConst.FILE_CHOOSER.setFileFilter( filter );
//        if ( GUIConst.LOAD_FILE_PATH != null && !GUIConst.LOAD_FILE_PATH.isEmpty() )
//            GUIConst.FILE_CHOOSER.setSelectedFile( new File( GUIConst.LOAD_FILE_PATH ));
        boolean confirmed = false;
        String fileName = "";
        
        while ( !confirmed ) {
            if ( GUIConst.FILE_CHOOSER.showDialog( this, java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("Save_Report") ) == JFileChooser.APPROVE_OPTION ) {
                fileName = GUIConst.FILE_CHOOSER.getSelectedFile().getAbsolutePath();
                GUIConst.LOAD_FILE_PATH = fileName;
                if ( GUIConst.FILE_CHOOSER.getSelectedFile().exists() ) {
                    switch ( JOptionPane.showConfirmDialog( this, java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("File_exists._Overwrite?"), java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("Please_Confirm"), 
                                            JOptionPane.YES_NO_CANCEL_OPTION )) {
                        case JOptionPane.YES_OPTION: confirmed = true; break;
                        case JOptionPane.NO_OPTION: break;
                        case JOptionPane.CANCEL_OPTION: { dispose(); return; }
                        default: { dispose(); return; }
                    } 
                } else
                    confirmed = true;
            } else {
                dispose();
                return;
            }
        } // end while loop
        dispose();
        final String choseFileName = fileName;
        final NewWaitingDialog waitDlg = new NewWaitingDialog();
        waitDlg.showUp( java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("Processing,_please_wait_..."),
                new TimerTask() {
            public void run() {
        
        if(myReportFile.saveReport( choseFileName, myReportType )){
//            dispose();
            if(waitDlg != null){
                waitDlg.unShow();
            }
            JOptionPane.showMessageDialog( null, java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("report_saved_suc"));
        }else{
            if(waitDlg != null){
                waitDlg.unShow();
            }
//            dispose();
            JOptionPane.showMessageDialog( null, java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("report_saved_fail"));
        }
          }
        });
    }//GEN-LAST:event_jButtonOKActionPerformed

    private void reportTypeSelected() {
        if ( jRadioTypeNoData.isSelected() )
            myReportType = ReportFile.SAVE_REPORT_WITHOUT_DATA;
        else if ( jRadioTypeWithData.isSelected() )
            myReportType = ReportFile.SAVE_REPORT_WITH_DATA;
    }
    
