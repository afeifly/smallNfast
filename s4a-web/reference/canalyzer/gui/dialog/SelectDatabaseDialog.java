    private void jButtonRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoveActionPerformed
        int[] indexes = jTableMain.getSelectedRows();
        if ( indexes.length <= 0 ) return;
        selectedIndex = indexes[0];        

        int reply;
        String tail = "";
        if ( selectedIndex == theCommonValue.getSelectedDatabaseIndex() ) {
            // this one is currently open
            reply = JOptionPane.showConfirmDialog( this, java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("Selected_database_is_currently_open._Are_you_sure_to_remove_it?"), 
                    java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("Please_confirm"), JOptionPane.YES_NO_OPTION );
            if ( reply != JOptionPane.OK_OPTION ) 
                return;
            theCommonValue.getDataBase().shutDown();
            tail = java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("<br><br>Please_select_another_one_to_open.<br><br>");
        } else {
            if ( selectedIndex < theCommonValue.getSelectedDatabaseIndex() ) {
                theCommonValue.setSelectedDatabaseIndex( theCommonValue.getSelectedDatabaseIndex() - 1 );
            }
        }
        
        myDatabaseInformations.remove( selectedIndex );
        selectedIndex = -1;
        addDatabaseInformation();
        JOptionPane.showMessageDialog( this, java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("<html>Database_connection_removed._") + tail + "</html>" );
        
    }//GEN-LAST:event_jButtonRemoveActionPerformed

    private void jButtonSelectFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSelectFileActionPerformed
        final JFileChooser fc = new JFileChooser();
        fc.setFileSelectionMode( JFileChooser.FILES_AND_DIRECTORIES );
        File dbDir;
        String fileName;
        
        fc.setSelectedFile(new File(jLabelDBPath.getText()));
        if ( fc.showOpenDialog(this) == JFileChooser.APPROVE_OPTION ) {
            
            dbDir = fc.getSelectedFile();            
            fileName = dbDir.getAbsolutePath();
            
            // get rid of the extension
            //fileName = fileName.substring( 0, fileName.lastIndexOf( "." ) );
            //jLabelDBPath.setText( fileName );
            if ( !dbDir.exists() ) {
                JOptionPane.showMessageDialog( this, java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("Directory_doesn't_exist._Please_try_again.") );
                return;
            }
            if ( !dbDir.isDirectory() ) {
                JOptionPane.showMessageDialog( this, java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("Please_select_a_directory_instead_of_a_file.") );
                return;
            }
            jLabelDBPath.setText( fileName );
            jLabelDBPath.setVisible(false);
            
            displayFilePath(fileName);
           
        }
    }//GEN-LAST:event_jButtonSelectFileActionPerformed

    private void displayFilePath(String fileName){
        
        if(fileName == null){
            jLabelDBPath1.setText( "" );
            return;
        }
        int len = fileName.length();
        int splitLen = 130 ;          
        int strFileNameCount = len/splitLen;          
        int strFileNameOther = len%splitLen;
        if(strFileNameOther > 0){
            strFileNameCount += 1 ;
        }

        String strTmp  = "<html>";
        String displayFileNamePath = fileName;
        for(int i = 0; i < strFileNameCount ; i++){
            if(i == (strFileNameCount-1)){
                strTmp += fileName.substring(splitLen*i,len)+"</html>";
            }else{
                strTmp += fileName.substring(splitLen*i,splitLen*(i+1))+"<br>";
            }   
            displayFileNamePath = strTmp;
        }

        jLabelDBPath1.setText( displayFileNamePath );
    }
    
