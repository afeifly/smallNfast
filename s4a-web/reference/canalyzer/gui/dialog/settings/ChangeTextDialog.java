    private void jButtonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOKActionPerformed
        try {
            myTexts.Title = jTextTitle.getText();
            getLegends();

            String s = jTextYAxis1.getText();
            if ( s.length() > 0 )
                myTexts.ValueAxises[0] = s;
            s = jTextYAxis2.getText();
            if ( s.length() > 0 )
                myTexts.ValueAxises[1] = s;
            s = jTextYAxis3.getText();
            if ( s.length() > 0 )
                myTexts.ValueAxises[2] = s;
            s = jTextYAxis4.getText();
            if ( s.length() > 0 )
                myTexts.ValueAxises[3] = s;
        } catch ( Exception e ) {}
        
        theCommonValue.setTexts( myTexts );
        dispose();
    }//GEN-LAST:event_jButtonOKActionPerformed

