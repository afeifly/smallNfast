    private void initTimeFields() {
        try {
            Timestamp stime, etime;
            
            stime = new Timestamp( startTime.getTimeInMillis() );
            jLabelStart.setText( GUIConst.DEFAULT_DATE_AND_TIME_FORMAT( stime ));
            jXDatePickerStart.setDate( startTime.getTime() );
            numModelStartHour.setValue( startTime.get( Calendar.HOUR_OF_DAY ) );
            //numModelStartMinute.setValue( startTime.get( Calendar.MINUTE ) );
            //numModelStartSecond.setValue( startTime.get( Calendar.SECOND ) );
       
            etime = new Timestamp( endTime.getTimeInMillis() );
            jLabelEnd.setText( GUIConst.DEFAULT_DATE_AND_TIME_FORMAT( etime ));
            jXDatePickerEnd.setDate( endTime.getTime() );
            numModelEndHour.setValue( endTime.get( Calendar.HOUR_OF_DAY ) );
            //numModelEndMinute.setValue( endTime.get( Calendar.MINUTE ) );
            //numModelEndSecond.setValue( endTime.get( Calendar.SECOND ) );

        } catch ( Exception e ) {
            System.out.println( "initTimeFields: " + e.getMessage() );
        }
        
    }
    
