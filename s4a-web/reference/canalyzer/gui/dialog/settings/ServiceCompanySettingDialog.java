    private void drawServiceLogo(String logoPath){  
       if(logoPath != null){
         //  System.out.println("drawServiceLogo logopath = "+logoPath);
           ImageIcon serviceLogoImage = new javax.swing.ImageIcon( logoPath );
           //serviceLogoImage = PrintManager.validateAndCreateImageIcon(logoPath);
           if (serviceLogoImage != null) {              
               Graphics2D g2d = (Graphics2D) jPanelMain.getGraphics();
               int logolableWidth = jLabelLogo.getWidth();
               // Note: logoHeight is 0 by default
               int logolableHeight = jLabelLogo.getHeight();
//System.out.println( "drawing " + jLabelLogo.getX() + " " + jLabelLogo.getY() + " " + logolableWidth + " " + logolableHeight + " " );
               g2d.drawImage(serviceLogoImage.getImage(), jLabelLogo.getX(), jLabelLogo.getY(), logolableWidth, logolableHeight, null);  
           }                               
       }
      
   }
    
