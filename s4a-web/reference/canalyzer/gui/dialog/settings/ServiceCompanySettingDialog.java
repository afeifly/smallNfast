    private void jButtonSelectLogoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSelectLogoActionPerformed
        final JFileChooser fc = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter(
        "JPG & GIF Images", "jpg", "gif","bmp","png");
        fc.setFileFilter(filter);
        if ( fc.showDialog( this, java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("Load") ) != JFileChooser.APPROVE_OPTION ) {
            return;
        }        
        long size = fc.getSelectedFile().length();
        if(size>200*1024){
            JOptionPane.showMessageDialog(this,
                        java.util.ResourceBundle.getBundle("com/cs/canalyzer/CAATexts").getString("Logo file size should not exceed 200 KByte")
                    );
            return;
        }
        myBasicSetting.ServiceLogoFilePath = fc.getSelectedFile().getAbsolutePath();
        repaint();
        //drawServiceLogo(myBasicSetting.ServiceLogoFilePath);
        //jLabelLogo.setIcon( new javax.swing.ImageIcon( myBasicSetting.ServiceLogoFilePath ));
    }//GEN-LAST:event_jButtonSelectLogoActionPerformed

