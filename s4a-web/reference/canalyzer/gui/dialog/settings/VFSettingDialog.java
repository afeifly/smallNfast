    private boolean verifyInput() {
        try {
            powerMin = Double.valueOf( GUIConst.VerifyString( jTextPowerMin.getText() ));
            powerP2 = Double.valueOf( GUIConst.VerifyString( jTextPowerP2.getText() ));
            powerP3 = Double.valueOf( GUIConst.VerifyString( jTextPowerP3.getText() ));
            powerMax = Double.valueOf( GUIConst.VerifyString( jTextPowerMax.getText() ));
            
            airDeliveryMin = Double.valueOf( GUIConst.VerifyString( jTextAirMin.getText() ));
            airDeliveryP2 = Double.valueOf( GUIConst.VerifyString( jTextAirP2.getText() ));
            airDeliveryP3 = Double.valueOf( GUIConst.VerifyString( jTextAirP3.getText() ));
            airDeliveryMax = Double.valueOf( GUIConst.VerifyString( jTextAirMax.getText() ));
            
            if ( powerMin > 0 && powerP2 > 0 && powerP3 > 0 && powerMax > 0
                && powerP2 > powerMin && powerP3 > powerP2 && powerMax > powerP3 
                && airDeliveryMin > 0 && airDeliveryP2 > 0 && airDeliveryP3 > 0 && airDeliveryMax > 0 
                && airDeliveryP2 > airDeliveryMin && airDeliveryP3 > airDeliveryP2 && airDeliveryMax > airDeliveryP3 ) {
                return true;
            } else
                return false;
        } catch( Exception e ) {
            return false;
        }
    }

