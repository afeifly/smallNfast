private void jButtonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOKActionPerformed
    if ( !verifyInput() ) {
        JOptionPane.showMessageDialog( this, ERROR_INPUT_MESSAGE );
        return;
    }
    
    myCompressor.VFMotorPower = VFConst.MOTOR_POWER_LIST[jComboMotorPower.getSelectedIndex()];
    myCompressor.VFSystemPressure = VFConst.SYSTEM_PRESSURE_LIST[jComboSystemPressure.getSelectedIndex()];
    myCompressor.VFPowerMin = powerMin;
    myCompressor.VFPowerP2 = powerP2;
    myCompressor.VFPowerP3 = powerP3;
    myCompressor.VFPowerMax = powerMax;
    myCompressor.VFAirDeliveryMin = airDeliveryMin;
    myCompressor.VFAirDeliveryP2 = airDeliveryP2;
    myCompressor.VFAirDeliveryP3 = airDeliveryP3;
    myCompressor.VFAirDeliveryMax = airDeliveryMax;
    
    if(myCompressor.hasPowerChannel()){
        myCompressor.calculateVFLinearCoefficiency();
    }else{
        myCompressor.calculateVFAmpAndLinearCoefficiency();
    }
    myCompressor.VFParameterSet = true;
    
    dispose();
}//GEN-LAST:event_jButtonOKActionPerformed

